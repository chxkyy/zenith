package com.zenith.admin.annotation;

/**
 * 数据权限控制策略枚举
 *
 * 各功能模块的开发者在开发该模块时，根据业务需求在后端代码中
 * 通过 {@link DataPermission} 注解为本模块指定使用哪种权限策略。
 */
public enum DataPermissionStrategy {

    /**
     * 纯组织架构控制
     * <p>
     * 按用户所属组织及其下级组织过滤数据。
     * 过滤依据：业务数据表中的 org_id 字段。
     * 适用场景：模块的数据天然按组织归属（如部门公告、组织配置等）。
     * </p>
     */
    ORG,

    /**
     * 人员-数据绑定 + 组织架构混合控制
     * <p>
     * 在组织层级基础上，增加人员与业务数据的精细绑定关系（通过 t_data_permission 表）。
     * 用户可查看：本人作为负责人的数据 + 所有下级用户负责的数据。
     * 适用场景：模块的数据需要精确到个人负责范围（如客户数据、产品数据）。
     * </p>
     */
    OWNER_ORG
}
